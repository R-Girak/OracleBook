public class Ch02_003_CharArithDemo {
    public static void main(String[] args) {
        char ch;
        ch = 'X';
        System.out.println("ch содержит " + ch);

        ch++;
        System.out.println("Теперь ch содержит " + ch);

        ch = 90;
        System.out.println("Теперь ch содержит " + ch);
    }
}
